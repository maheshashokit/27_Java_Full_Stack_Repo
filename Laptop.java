public class Laptop {

	private String brandName;
	private float cost;

	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}

	public void setCost(float cost) {
		this.cost = cost;
	}

	public String getBrandName() {
		return brandName;
	}

	public float getCost() {
		return cost;
	}
}