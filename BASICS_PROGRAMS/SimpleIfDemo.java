class SimpleIfDemo 
{
	public static void main(String[] args) {

		//Always we need the required code inside the main() only
		System.out.println("Hello Mahesh, Started If Program!!!!!");

        //Declared one variable in java program
		int a = 10;

		//writing the if condition
		if(a == 10){
		   System.out.println("Condition Got Passed because a = 10 !!!!!");
		}

		System.out.println("Hello Mahesh, End If Program!!!!");		
	}
}