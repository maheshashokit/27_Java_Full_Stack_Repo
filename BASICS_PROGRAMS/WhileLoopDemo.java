class WhileLoopDemo 
{
	public static void main(String[] args) 
	{
		int loopStart = 10;
		int loopStart1 = 10;

		while(loopStart >= 1){
		
		   System.out.println("loop start =" + loopStart);

		   loopStart--;
		}

		System.out.println("==============================");	
		
		while(loopStart1 != 0){
		
		   System.out.println("loop Second Loop =" + loopStart1);

		   loopStart1--;
		}

	}
}
