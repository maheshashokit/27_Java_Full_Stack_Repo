//This program will demonstrates on Relational Operators
class RelationalOperatorsDemo 
{
	public static void main(String[] args) 
	{
		int a = 20, b= 50;

		System.out.println("GreaterThan  Operators              :::::" + (a > b));
		System.out.println("LessThan     Operators              :::::" + (a < b));
		System.out.println("Equal To     Operators              :::::" + (a == b));
		System.out.println("Not EqualTo  Operators              :::::" + (a != b));
		System.out.println("GreaterThan Equal To  Operators     :::::" + (a >= b));
		System.out.println("LessThan    Equal To  Operators     :::::" + (a <= b));
	}
}
