//This program demonstrated Basic Arthimatic Operation using Java
class ArthimeticOperatorsDemo 
{
	public static void main(String[] args) 
	{
		System.out.println("Hello World!");	
		
		//Defining the two variables
		int a = 10;
		int b = 20;

		//Displaying values from variables
		System.out.println("a =" + a);
		System.out.println("b =" + b);

		//Perform the Arthimatic Operations
		System.out.println("Addition Operations        ::::" +  (a + b));
		System.out.println("Subtraction Operations     ::::" +  (a - b));
		System.out.println("Multiplication Operations  ::::" +  (a * b));
		System.out.println("Division Operations        ::::" +  (b / a));
		System.out.println("Modulo Operations          ::::" +  (a % b));
	}
}